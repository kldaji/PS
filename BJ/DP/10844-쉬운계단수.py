from sys import *

"""
<오답>
"""


# 수의 길이 (1 ~ 100)
N = int(stdin.readline().rstrip())

"""
<Example> (0으로 시작하는 수는 없다.)
1) N = 1 : 1 ~ 9 (9개)
2) N = 2 : 10, 12, 21, 23, ..., 98 (2*9-1 = 17개)
3) N = 3 : 101, 123, 121, 210, 212, ..., 987, 989 (2*17-1 = 33개)

dp[i] = 2*(dp[i-1]) - 1
"""
dp = [0 for _ in range(101)]
dp[1] = 9

for i in range(2, N + 1):
    dp[i] = 2 * dp[i - 1] - 1

# 정답 : 1,000,000,000으로 나눈 나머지.
print(dp[N] % 1000000000)
